    /*
    Dưới đây là sự phân biệt giữa ArrayList, List, Stack và Queue:

    ArrayList:
        ArrayList là một lớp cụ thể trong Java Collections Framework và cũng là một dạng cấu trúc dữ liệu danh sách (List).
        ArrayList lưu trữ các phần tử theo thứ tự và có thể thay đổi kích thước động.
            Nó cung cấp các phương thức để thêm, xóa, truy cập và tìm kiếm phần tử trong danh sách.
        ArrayList được triển khai dưới dạng một mảng động, trong đó các phần tử được lưu trữ liên tiếp trong bộ nhớ.
    List:
        List là một giao diện (interface) trong Java mô tả các hoạt động liên quan đến danh sách.
        List mô tả các tính chất chung của các cấu trúc dữ liệu danh sách như ArrayList, LinkedList, Vector và Stack.
        List cung cấp các phương thức để thêm, xóa, truy cập và tìm kiếm phần tử trong danh sách.
        List lưu trữ các phần tử theo thứ tự và có thể chứa các phần tử trùng lặp.
    Stack:
        Stack là một cấu trúc dữ liệu dạng ngăn xếp (LIFO - Last In, First Out).
        Trong Stack, phần tử cuối cùng được thêm vào là phần tử đầu tiên được lấy ra.
        Stack cung cấp các phương thức push (đẩy phần tử vào ngăn xếp),
         pop (lấy phần tử từ ngăn xếp) và peek (xem phần tử trên cùng mà không xóa nó).
        Các phép toán trên Stack được thực hiện trên phần tử trên cùng (top) của ngăn xếp.
    Queue:
        Queue là một cấu trúc dữ liệu dạng hàng đợi (FIFO - First In, First Out).
        Trong Queue, phần tử đầu tiên được thêm vào là phần tử đầu tiên được lấy ra.
        Queue cung cấp các phương thức enqueue (đưa phần tử vào hàng đợi), dequeue (lấy phần tử từ hàng đợi) và
        peek (xem phần tử đầu tiên mà không xóa nó).
        Các phép toán trên Queue được thực hiện trên phần tử đầu tiên (front) và phần tử cuối cùng (rear) của hàng đợi.
    Tóm lại, ArrayList và List là các cấu trúc dữ liệu danh sách (List) trong Java,
     trong khi Stack và Queue là các cấu trúc dữ liệu đặc biệt (Stack theo nguyên tắc LIFO và Queue theo nguyên tắc FIFO).
     */
